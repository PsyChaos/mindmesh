# --- normal files pass ---

def test_normal_file_passes() -> None:
    f = _make_filter()
    fc = _fc("src/main.py", "def hello():\n    return 'world'\n")
    passed, _ = f.filter([fc])
    assert len(passed) == 1
    assert passed[0].path == "src/main.py"


# --- FilterReport counts ---

def test_filter_report_counts() -> None:
    f = _make_filter(block_files=["*.key"], max_file_size_kb=1)
    files = [
        _fc("secret.key", "should be blocked by policy"),
        _fc("big.py", ("y" * 100 + "\n") * 12),  # ~1.2 KB → size blocked, avg line 100
        _fc("ok.py", "small"),
    ]
    _, report = f.filter(files)
    assert report.total_candidates == 3
    assert report.passed == 1
    assert "secret.key" in report.blocked_by_policy
    assert "big.py" in report.blocked_by_size


# --- edge cases ---

def test_empty_input_returns_empty() -> None:
    f = _make_filter()
    passed, report = f.filter([])
    assert passed == []
    assert report.total_candidates == 0
    assert report.passed == 0


def test_all_blocked_passed_is_zero() -> None:
    f = _make_filter(block_files=["*.py"])
    files = [_fc("a.py"), _fc("b.py"), _fc("c.py")]
    passed, report = f.filter(files)
    assert passed == []
    assert report.passed == 0
    assert report.total_candidates == 3
