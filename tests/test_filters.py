"""Tests for ContextFilter."""


from mindmesh.config import LimitsConfig, PrivacyConfig
from mindmesh.context.collector import FileContext
from mindmesh.context.filters import ContextFilter, is_binary, is_generated_or_minified
from mindmesh.policy.file_policy import FilePolicy


def _make_filter(
    block_files: list[str] | None = None,
    block_dirs: list[str] | None = None,
    max_file_size_kb: int = 120,
    max_total_context_kb: int = 1024,
) -> ContextFilter:
    privacy = PrivacyConfig(
        block_files=block_files or [],
        block_dirs=block_dirs or [],
    )
    limits = LimitsConfig(
        max_file_size_kb=max_file_size_kb,
        max_total_context_kb=max_total_context_kb,
    )
    return ContextFilter(FilePolicy(privacy), limits)


def _fc(path: str, content: str = "hello world\n") -> FileContext:
    return FileContext(path=path, content=content, language="python", scope_type="file")


# --- policy block ---

def test_policy_blocked_file_filtered() -> None:
    f = _make_filter(block_files=["*.env"])
    passed, report = f.filter([_fc(".env")])
    assert passed == []
    assert ".env" in report.blocked_by_policy


def test_policy_blocked_dir_filtered() -> None:
    f = _make_filter(block_dirs=["node_modules"])
    passed, report = f.filter([_fc("node_modules/index.js")])
    assert passed == []
    assert "node_modules/index.js" in report.blocked_by_policy


