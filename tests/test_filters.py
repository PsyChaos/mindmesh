# --- file size ---

def test_large_file_blocked_by_size() -> None:
    # Multi-line so avg line length stays short (avoids generated detection)
    big_content = ("x" * 100 + "\n") * 1300  # ~127 KB
    f = _make_filter(max_file_size_kb=120)
    passed, report = f.filter([_fc("big.py", big_content)])
    assert passed == []
    assert "big.py" in report.blocked_by_size


# --- total context limit ---

def test_total_limit_cuts_remaining_files() -> None:
    # Each file ~10 KB (multi-line), limit 25 KB → first 2 pass, 3rd and beyond blocked
    chunk = ("x" * 100 + "\n") * 102  # ~10.1 KB, avg line 100 chars
    files = [_fc(f"f{i}.py", chunk) for i in range(4)]
    f = _make_filter(max_total_context_kb=25)
    passed, report = f.filter(files)
    assert len(passed) == 2
    assert len(report.blocked_by_total_limit) == 2


def test_total_limit_all_blocked_when_first_exceeds() -> None:
    chunk = ("x" * 100 + "\n") * 102  # ~10.1 KB
    files = [_fc(f"f{i}.py", chunk) for i in range(3)]
    f = _make_filter(max_total_context_kb=5)
    passed, report = f.filter(files)
    assert passed == []
    assert len(report.blocked_by_total_limit) == 3


